)

missing_files <- required_files[
  !file.exists(file.path(project_root, required_files))
]

if (length(missing_files) > 0L) {
  stop(
    paste0(
      "Run this script from the CancerPPIr repository root.\n",
      "Missing files:\n",
      paste0("- ", missing_files, collapse = "\n")
    ),
    call. = FALSE
  )
}

output_files <- c(
  "tests/testthat/test-cli.R",
  "scripts/run_r01_smoke_test.R",
  ".github/workflows/r-tests.yml"
)

existing_outputs <- output_files[
  file.exists(file.path(project_root, output_files))
]

if (length(existing_outputs) > 0L) {
  stop(
    paste0(
      "Phase 3.2 files already exist. Inspect them before rerunning setup:\n",
      paste0("- ", existing_outputs, collapse = "\n")
    ),
    call. = FALSE
  )
}

write_utf8 <- function(path, text) {
  dir.create(
    dirname(path),
    recursive = TRUE,
    showWarnings = FALSE
  )

  writeLines(
    enc2utf8(text),
    con = path,
    useBytes = TRUE
  )
}

