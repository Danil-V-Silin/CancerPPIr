files <- list(
  "tests/testthat/test-cli.R" = test_cli,
  "scripts/run_r01_smoke_test.R" = smoke_runner,
  ".github/workflows/r-tests.yml" = workflow
)

for (r_file in c(
  "tests/testthat/test-cli.R",
  "scripts/run_r01_smoke_test.R"
)) {
  temporary_r_file <- tempfile(fileext = ".R")
  writeLines(
    files[[r_file]],
    con = temporary_r_file,
    useBytes = TRUE
  )
  invisible(parse(file = temporary_r_file))
  unlink(temporary_r_file, force = TRUE)
}

for (relative_path in names(files)) {
  write_utf8(
    file.path(project_root, relative_path),
    files[[relative_path]]
  )
}

