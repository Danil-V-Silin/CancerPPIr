# - CLI validation tests
# - one local R01 integration-smoke runner
# - GitHub Actions workflow for automated unit and CLI tests
#
# Run from the repository root:
#   Rscript scripts/setup_phase3_ci_smoke.R

project_root <- normalizePath(
  ".",
  winslash = "/",
  mustWork = TRUE
)

required_files <- c(
  "cancerppir.R",
  "R/load_all.R",
  "R/07_pipeline.R",
